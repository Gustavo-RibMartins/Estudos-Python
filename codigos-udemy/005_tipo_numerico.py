"""
Tipo Numerico
"""

# 1 - Divisão com duas barras (//) retorna só a parte inteira da divisão

print(5 // 2)

# 2 - Resto da divisão se usa %

print(7 % 2)

# 3 - Potência é representada por dois asteriscos (**)

print(3 ** 3)

# 4 - Em JAVA, o maior int é 2**63, em Python, não há limite, apenas o da sua memória

print(2**1000)

# 5 - Soma de um número com ele mesmo

num = 1
num += 1  # posso usar -=, *=, /=
print(num)

# 6 - Função type() - vocÊ passa o valor e ela te retorna o tipo de dado
print(type(num))
