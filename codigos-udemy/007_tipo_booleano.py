"""
Tipo Booleano

Algebra Booleana - criada por George Boole

2 constantes - verdadeiro ou falso (True ou False)

OBS: Sempre com a inicial maiúscula
"""

ativo = True
print(ativo)

"""
Operações básicas
"""

# 1 - Negação (not)

print(not ativo)

# 2 - Ou (or)

ativo = True
logado = False
print(ativo or logado)

# 3 - E (and)

ativo = True
logado = False
print(ativo and logado)
